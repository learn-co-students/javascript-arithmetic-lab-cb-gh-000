function add(a, b){
  var sum=0;
  sum = a+b;
  return sum;
}
function subtract(a, b){
  var x=0;
  x = a-b;
  return x;
}
function multiply(a, b){
  var y=0;
  y = a*b;
  return y;
}
function divide(a, b){
  var c=0;
  c = a/b;
  return c;
}
function inc(a){
  a++
  return a;
}
function dec(a){
  a=a-1;
  return a;
}
