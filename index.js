function add(a, b){
  return a + b
}

function subtract(a,b){
  return a - b
}

function multiply(a, b){
  return a * b
}

function divide(a, b){
  return a/b
}

function increment(n){
  return ++n
}
function decrement(n){
  return --n
}

function makeInt(n){
  var res = parseInt(n, 10);
  return res
}

function preserveDecimal(n){
  var dec = parseFloat(n);
  return dec

}
