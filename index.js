function add(num_1, num_2) {
  return num_1 + num_2
}

function subtract(num_1, num_2) {
  return num_1 - num_2
}

function multiply(num_1, num_2) {
  return num_1 * num_2
}

function divide(num_1, num_2) {
  return num_1 / num_2
}

function inc(num) {
  return ++num;
}

function dec(num) {
  return --num;
}

function makeInt(n) {
  return parseInt(n,10);
}

function preserveDecimal(n) {
  return parseFloat(n,10);
}
