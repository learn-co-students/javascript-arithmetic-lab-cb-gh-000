function add(a, b) {
  var result = a + b
  return result
}

function subtract(a, b) {
  var result = a - b
  return result
}

function multiply(a, b) {
  var result = a * b
  return result
}

function divide(a, b) {
  var result = a / b
  return result
}

function inc(n) {
  ++n
  return n
}

function dec(n) {
  --n
  return n
}

function makeInt(a) {
  var result = parseInt(a, 10)
  return result
}

function preserveDecimal(a) {
  var result = parseFloat(a)
  return result
}
